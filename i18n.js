/* ═══════ LANGUAGE ENGINE ═══════ */
const KlyxI18n = {
  current: 'ar',

  init() {
    this.current = localStorage.getItem('klyxLang') || 'ar';
    this.apply();
  },

  set(lang) {
    this.current = lang;
    localStorage.setItem('klyxLang', lang);
    this.apply();
  },

  t(key) {
    const t = KLYX_TRANSLATIONS[this.current];
    return t && t[key] !== undefined ? t[key] : (KLYX_TRANSLATIONS['en'][key] || key);
  },

  apply() {
    const t = KLYX_TRANSLATIONS[this.current];
    if (!t) return;
    document.documentElement.lang = t.lang;
    document.documentElement.dir = t.dir;
    document.documentElement.setAttribute('data-lang', this.current);

    // Apply all data-i18n elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = this.t(key);
      if (val !== undefined) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = val;
        } else if (el.getAttribute('data-i18n-html')) {
          el.innerHTML = val;
        } else {
          el.textContent = val;
        }
      }
    });

    // Update switcher active state
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === this.current);
    });

    // Dispatch event so pages can react
    document.dispatchEvent(new CustomEvent('klyxLangChanged', { detail: { lang: this.current, t } }));
  }
};

/* ═══════ THEME ENGINE ═══════ */
const KlyxTheme = {
  current: 'purple',

  init(storageKey) {
    this.storageKey = storageKey || 'klyxCardTheme';
    this.current = localStorage.getItem(this.storageKey) || 'purple';
    this.applyToCard();
  },

  set(theme) {
    this.current = theme;
    localStorage.setItem(this.storageKey, theme);
    // ✅ FIX: always sync to shared key so card.html can read it
    localStorage.setItem('klyxCardTheme', theme);
    this.applyToCard();
    document.querySelectorAll('.theme-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.theme === theme);
    });
    document.dispatchEvent(new CustomEvent('klyxThemeChanged', { detail: { theme, config: KLYX_THEMES[theme] } }));
  },

  get() { return KLYX_THEMES[this.current] || KLYX_THEMES.purple; },

  applyToCard() {
    const th = this.get();
    const root = document.documentElement;
    root.style.setProperty('--card-primary', th.primary);
    root.style.setProperty('--card-secondary', th.secondary);
    root.style.setProperty('--card-accent', th.accent);
    root.style.setProperty('--card-grad', th.grad);
    root.style.setProperty('--card-glow', th.glow);
    root.style.setProperty('--card-bg1', th.bg1);
    root.style.setProperty('--card-bg2', th.bg2);
    root.style.setProperty('--card-bg3', th.bg3);
  }
};

/* ═══════ LANGUAGE SWITCHER HTML ═══════ */
function injectLangSwitcher(container) {
  const sw = document.createElement('div');
  sw.className = 'klyx-lang-switcher';
  sw.innerHTML = `
    <button class="lang-btn${KlyxI18n.current==='ar'?' active':''}" data-lang="ar" onclick="KlyxI18n.set('ar')">ع</button>
    <button class="lang-btn${KlyxI18n.current==='fr'?' active':''}" data-lang="fr" onclick="KlyxI18n.set('fr')">FR</button>
    <button class="lang-btn${KlyxI18n.current==='en'?' active':''}" data-lang="en" onclick="KlyxI18n.set('en')">EN</button>
  `;
  (container || document.body).appendChild(sw);
}

